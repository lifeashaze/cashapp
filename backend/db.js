import mongoose from "mongoose";
const { Schema } = mongoose;

mongoose.connect("mongodb+srv://admin:admin@cluster0.mzzxlpo.mongodb.net/")

const userSchema = new Schema({
    username: {
        type: String,
        required: true,
        unique: true,
        trim: true,
        lowercase: true,
        minLength: 3,
        maxLength: 30
    },
    password: {
        type: String,
        required: true,
        minLength: 6
    },
    firstName: {
        type: String,
        required: true,
        trim: true,
        maxLength: 50
    },
    lastName: {
        type: String,
        required: true,
        trim: true,
        maxLength: 50
    }
});

const accountSchema = new Schema({
    userId: {
        type: mongoose.Schema.Types.ObjectId, 
        ref: 'User',
        required: true
    },
    balance: {
        type: Number,
        required: true
    }
})

const User = mognoose.model('User',userSchema);
const Account = mognoose.model('Account',accountSchema);

module.exports = {
    User,
    Account 
};